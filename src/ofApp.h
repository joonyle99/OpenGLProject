/*
 * ============================================
 *          Screen 좌표계 (픽셀 좌표)
 * ============================================
 *
 *    (0,0)------------------------------> X (Width)
 *      |
 *      |   +-------------------------+
 *      |   |                         |
 *      |   |                         |
 *      |   |        Screen           |
 *      |   |                         |
 *      |   |                         |
 *      |   +-------------------------+
 *      v
 *    Y (Height)
 *
 *    - 원점: 좌측 상단 (0, 0)
 *    - X축: 오른쪽으로 증가 (0 ~ Width)
 *    - Y축: 아래쪽으로 증가 (0 ~ Height)
 *    - 단위: 픽셀
 *
 *
 * ============================================
 *					NDC 좌표계
 * ============================================
 *
 *                      Y (+1)
 *                        ^
 *                        |
 *        (-1,+1)         |         (+1,+1)
 *              +---------+---------+
 *              |         |         |
 *              |         |         |
 *    X (-1) <--+---------+---------+--> X (+1)
 *              |       (0,0)       |
 *              |         |         |
 *              +---------+---------+
 *        (-1,-1)         |         (+1,-1)
 *                        |
 *                        v
 *                      Y (-1)
 *
 *    - 원점: 화면 중앙 (0, 0)
 *    - X축: -1 (좌) ~ +1 (우)
 *    - Y축: -1 (하) ~ +1 (상)
 *    - 단위: 정규화된 값 (-1 ~ +1)
 *
 *
 * ============================================
 *            UV 좌표계 (텍스처 좌표)
 * ============================================
 *
 *      V
 *      ^
 *      |
 *      |
 *    (0,1)                         (1,1)
 *      +-----------------------------+
 *      |                             |
 *      |                             |
 *      |          Texture            |
 *      |                             |
 *      |                             |
 *      +-----------------------------+------> U
 *    (0,0)                         (1,0)
 *
 *    - 원점: 좌측 하단 (0, 0)
 *    - U축: 오른쪽으로 증가 (0 ~ 1)
 *    - V축: 위쪽으로 증가 (0 ~ 1)
 *    - 단위: 정규화된 값 (0 ~ 1)
 *    - 주의: DirectX는 V축이 반대 (좌측 상단이 원점)
 *
 *
 * ============================================
 *              좌표 변환 공식
 * ============================================
 *
 *    Screen -> NDC:
 *        ndc.x = (screen.x / width) * 2.0 - 1.0
 *        ndc.y = 1.0 - (screen.y / height) * 2.0
 *
 *    NDC -> Screen:
 *        screen.x = (ndc.x + 1.0) * 0.5 * width
 *        screen.y = (1.0 - ndc.y) * 0.5 * height
 *
 *    NDC -> UV (OpenGL):
 *        uv.u = (ndc.x + 1.0) * 0.5
 *        uv.v = (ndc.y + 1.0) * 0.5
 *
 *    UV -> NDC (OpenGL):
 *        ndc.x = uv.u * 2.0 - 1.0
 *        ndc.y = uv.v * 2.0 - 1.0
 *
 *    Screen -> UV (OpenGL):
 *        uv.u = screen.x / width
 *        uv.v = 1.0 - (screen.y / height)
 *
 */

 /*
  * ============================================
  *      이미지 파일 메모리 저장 순서 (PNG, JPG 등)
  * ============================================
  *
  *    메모리 시작 (offset 0)
  *      |
  *      v
  *      +-----------------------------+  <- 첫 번째 행 (y=0, 상단)
  *      |  Row 0 (Top)                |
  *      +-----------------------------+
  *      |  Row 1                      |
  *      +-----------------------------+
  *      |  Row 2                      |
  *      +-----------------------------+
  *      |  ...                        |
  *      +-----------------------------+
  *      |  Row N (Bottom)             |  <- 마지막 행 (하단)
  *      +-----------------------------+
  *
  *    - 상단 행부터 순차적으로 저장
  *    - 대부분의 이미지 포맷이 이 방식 사용
  *
  *
  * ============================================
  *      OpenGL 텍스처 메모리 해석 방식
  * ============================================
  *
  *      V (+1)
  *      ^
  *      |
  *      +-----------------------------+  <- V=1 (상단)
  *      |  Row N (Top in OpenGL)      |
  *      +-----------------------------+
  *      |  ...                        |
  *      +-----------------------------+
  *      |  Row 1                      |
  *      +-----------------------------+
  *      |  Row 0 (Bottom in OpenGL)   |  <- V=0 (하단), 메모리 시작
  *      +-----------------------------+------> U
  *
  *    - OpenGL은 메모리의 첫 바이트를 V=0 (하단)으로 해석
  *    - 즉, 메모리 시작 = 텍스처 하단
  *
  */